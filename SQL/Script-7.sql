CREATE TABLE AE_USERS (
AEID NUMBER(10) PRIMARY KEY,
EMAIL VARCHAR2(255) NOT NULL,
PASSWORD VARCHAR2(255) NOT NULL,
ADMINLVL NUMBER(2) NOT NULL,
STATUS NUMBER(2) NOT NULL,
BLOCKLIST NUMBER(10) --foreign key
);

CREATE SEQUENCE AE_USER_seq
START WITH
1 INCREMENT BY 1;

CREATE TABLE AE_CHANNELS (
CHANNELID NUMBER (10) PRIMARY KEY,
CHANNELNAME VARCHAR2 (255) NOT NULL,
CHANNELTYPE NUMBER (2),
ACTIVEUSERS NUMBER(10), --foreign key
BLOCKEDUSERS NUMBER(10)  --foriegn KEY
);

CREATE SEQUENCE AE_CHANNELS_seq
START WITH
1 INCREMENT BY 1;


CREATE TABLE AE_COMMENTS (
COMMENTID NUMBER(10) PRIMARY KEY,
CHANNEL_ID NUMBER(10) NOT NULL, --forieng key
USERID NUMBER(10) NOT NULL, --forieng key
COMMENTS VARCHAR2(4000),
PICTURE VARCHAR2(4000),
POSTDATE VARCHAR2(255)
);

CREATE SEQUENCE AE_COMMENTS_seq
START WITH
1 INCREMENT BY 1;


